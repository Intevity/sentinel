interface Captured {
  url: string;
  body: Buffer;
}

interface FakeUpstream {
  server: Server;
  url: string;
  received: Captured[];
}

async function startFake(): Promise<FakeUpstream> {
  const received: Captured[] = [];
  const server = createServer((req: IncomingMessage, res) => {
    const chunks: Buffer[] = [];
    req.on('data', (c: Buffer) => chunks.push(c));
    req.on('end', () => {
      received.push({ url: req.url ?? '', body: Buffer.concat(chunks) });
      res.writeHead(200);
      res.end('{}');
    });
  });
  await new Promise<void>((resolve) => server.listen(0, '127.0.0.1', () => resolve()));
  const addr = server.address();
  if (typeof addr !== 'object' || addr === null) throw new Error('listen failed');
  return { server, url: `http://127.0.0.1:${addr.port}`, received };
}

function stopFake(f: FakeUpstream): Promise<void> {
  return new Promise((resolve) => f.server.close(() => resolve()));
}

function makeSettings(overrides: Partial<Settings> = {}): Settings {
  return {
    ...DEFAULT_SETTINGS,
    otelForwardingEnabled: true,
    otelForwardMetrics: true,
    otelForwardLogs: true,
    otelEmitSentinelMetrics: true,
    otelExporterHeaderName: 'k',
    // Stable, well-formed UUID v4 so tests assert exact-equality. Real
    // installs get one auto-generated by `coerce()`.
    otelServiceInstanceId: '11111111-2222-4333-8444-555555555555',
    ...overrides,
  };
}

async function waitFor(predicate: () => boolean, timeoutMs = 1500): Promise<void> {
  const deadline = Date.now() + timeoutMs;
  while (Date.now() < deadline) {
    if (predicate()) return;
    await new Promise((r) => setTimeout(r, 5));
  }
  throw new Error('waitFor timed out');
}

